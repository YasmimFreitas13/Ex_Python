#4 - Peça ao aluno para inserir suas notas em quatro matérias e calcule a média dessas notas.**

nota_1 = float(input('Insira a nota da primeira materia: '))
nota_2 = float(input('Insira a nota da segunda materia: '))
nota_3 = float(input('Insira a nota da terceira materia: '))
nota_4 = float(input('Insira a nota da quarta materia: '))

media = (nota_1 + nota_2 + nota_3 + nota_4) / 4

print('A média duas suas notas é: ', media)